function suma (n1, n2, n3) { //Función que contiene los argumentos que se tomaran en cuenta
	const resultado = n1 + n2 + n3;
	console.log(`El resultado es: ${resultado}`); // Muestra el resultado en la consola
}

const n1 = 3;
const n2 = 4;
const n3 = 8; //Valores definidos con const ya que este tipo de variable no admite undefined

suma(n1, n2, n3); // Llamada a la función
